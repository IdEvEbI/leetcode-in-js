/**
 * @param {TreeNode} root
 * @return {number[]}
 */
var inorderTraversal = function (root) {
  let res = [];
  let stack = [];
  let node = root;

  while (node || stack.length) {
    // 压栈左子树节点
    while (node) {
      stack.push(node);
      node = node.left;
    }

    // 出栈
    node = stack.pop();
    res.push(node.val);

    node = node.right;
  }

  return res;
};
// @lc code=end

var inorderTraversalRecursion = function (root) {
  const res = [];

  const inorder = (root) => {
    if (!root) {
      return;
    }
    // 左
    inorder(root.left);
    // 根
    res.push(root.val);
    // 右
    inorder(root.right);
  }

  inorder(root);

  return res;
};